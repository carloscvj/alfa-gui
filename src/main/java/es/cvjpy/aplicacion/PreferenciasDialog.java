    private ModeloGuiPro auxiliar = new ModeloGuiPro(this);
    private PreferenciasCli datos;
    public static final String PROP_DATOS = "datos";

    public PreferenciasCli getDatos() {
        return datos;
    }

    public void setDatos(PreferenciasCli datos) {
        PreferenciasCli oldDatos = this.datos;
        this.datos = datos;
        firePropertyChange(PROP_DATOS, oldDatos, datos);
    }

    private void refresh() {
        firePropertyChange(PROP_DATOS, null, datos);
        aspectoSwingPanel1.refresh();
        repaint();
    }

    @Override
    public void inicio(PrincipalUIPro padre) {
        auxiliar.inicio(padre);
        PreferenciasCli misdatos = new PreferenciasCli();
        misdatos.setPrincipalPro(padre.getServidor());
        setDatos(misdatos);
        setVisible(true);
        refresh();
    }

    @Override
    public boolean puedosalir() {
        return auxiliar.puedosalir();
    }

    @Override
    public void fin() {
        if (auxiliar.getPadre() == null) {
            System.exit(0);
        } else {
            setVisible(false);
        }
    }

    private void guardar() {
        try {
            getDatos().guardarPreferencias();
            setVisible(false);
            if (auxiliar.getPadre() != null) {
                java.awt.EventQueue.invokeLater(new Runnable() {
                    @Override
                    public void run() {
                        Thread t = new Thread(new Runnable() {
                            @Override
                            public void run() {
                                setVisible(false);
                                auxiliar.getPadre().reiniciar();
                            }
                        });
                        t.start();

                    }
                });
            }
        } catch (Exception ex) {
            Logger.getLogger(PreferenciasDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    @Override
    public boolean isIniciado() {
        return auxiliar.isIniciado();
    }

    @Override
    public void rellama() {
        setVisible(true);
    }

    private void ensenarFileSystem() {
        try {
            selectorCarpeta.setSelectedFile(new File(datos.getPreferencias().getCarpetaSeguridad()));
            if (JFileChooser.APPROVE_OPTION == selectorCarpeta.showOpenDialog(this)) {

                datos.getPreferencias().setCarpetaSeguridad(selectorCarpeta.getSelectedFile().getAbsolutePath());
                campoCarpetaInicial.setText(datos.getPreferencias().getCarpetaSeguridad());
                refresh();

            }
        } catch (Exception ex) {
            Logger.getLogger(PreferenciasDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

